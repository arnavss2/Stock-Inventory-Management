    private void chkvenItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_chkvenItemStateChanged
        // TODO add your handling code here:
        if(chkven.isSelected()==true)
        {
            cbven.setVisible(true);
            cbven.removeAllItems();
            try{
                Class.forName("java.sql.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost/stockinventorymanagement","root","tabla10a");
                Statement stmt = con.createStatement();
                String query = "SELECT vendorName FROM vendorMaster;";
                ResultSet rs = stmt.executeQuery(query);
                while(rs.next())
                {
                    String vName = rs.getString(1);
                    cbven.addItem(vName);
                }
                rs.close();
                stmt.close();
                con.close();
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null,e);
            }
        }
        if(chkven.isSelected()==false)
        {
            cbven.setVisible(false);
        }
    }//GEN-LAST:event_chkvenItemStateChanged

