    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        String iname=txtiname.getText();
        String cat="",ven="",ref="",mins="",maxs="";
        int min = 0, max = 0;
        if(btns.isSelected())
        {
            cat="Surgical";
        }
        else if(btnns.isSelected())
        {
            cat="Non-Surgical";
        }
        
        if(chkven.isSelected())
        {
            ven=cbven.getSelectedItem().toString();
        }
        else
        {
            ven="N/A";
        }
        if(chkref.isSelected()){
            ref="Yes";
        }
        else{
            ref="No";
        }
        mins=txtmin.getText();
        maxs=txtmax.getText();
        
        if(iname.equals("")||cat.equals("")||mins.equals("")||maxs.equals("")){
            JOptionPane.showMessageDialog(null,"Please fill in all the required fields!");
        }
        else
        {
            min=Integer.parseInt(mins);
            max=Integer.parseInt(maxs);
            if(min>=max)
            {
                JOptionPane.showMessageDialog(null,"Minimum Quantity must be less than Maximum Quantity!");
            }
            else{
                        int x,c=0;
                        int minslength = mins.length();
                        for(x=0;x<minslength;x++)
                        {
                            char a = mins.charAt(x);
                            if(a<=57&&a>=48)
                            {
                                continue;
                            }
                            else
                            {
                                c++;
                            }
                        }
                        int maxslength = maxs.length();
                        for(x=0;x<maxslength;x++)
                        {
                            char a = maxs.charAt(x);
                            if(a<=57&&a>=48)
                            {
                                continue;
                            }
                            else
                            {
                                c++;
                            }
                        }
                        if(c!=0)
                        {
                            JOptionPane.showMessageDialog(null,"Enter numbers in Minimum Quantity and Maximum Quantity!");
                        }
                        else{
                                        try{
                                            Class.forName("java.sql.Driver");
                                            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/stockinventorymanagement","root","tabla10a");
                                            Statement stmt = con.createStatement();
                                            String query="SELECT ITEMNAME FROM ITEMMASTER";
                                            ResultSet rs=stmt.executeQuery(query);
                                            rs.next();
                                            if(rs.getString("itemname").equalsIgnoreCase(iname))
                                            JOptionPane.showMessageDialog(null,"Repetition of Item Name is not allowed!");                                                                                          
                                            else
                                                {                                         
                                                String insert = "INSERT INTO itemMaster VALUES('"+iname+"','"+cat+"','"+ven+"','"+ref+"',"+min+","+max+");";
                                                stmt.executeUpdate(insert);
                                                JOptionPane.showMessageDialog(null,"Record Saved successfully.");
                                                txtiname.setText("");
                                                bgcat.clearSelection();
                                                chkven.setSelected(false);
                                                txtmin.setText("");
                                                txtmax.setText("");
                                                chkref.setSelected(false);
                                                rs.close();
                                                stmt.close();
                                                con.close();                                            
                                           }                                    
                                        }
                                        catch(Exception e){
                                            JOptionPane.showMessageDialog(null,e);
                                        }
                        }
            }
        }
    }//GEN-LAST:event_btnsaveActionPerformed

